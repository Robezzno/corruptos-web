// ─── i18n engine ───
const i18n = {
  currentLang: 'es',

  init() {
    // Validar el idioma guardado: un 'ca' de la época trilingüe cae a 'es'.
    const saved = localStorage.getItem('corruptos-lang');
    const browserLang = navigator.language?.slice(0, 2);
    this.currentLang = (saved && TRANSLATIONS[saved]) ? saved
      : (TRANSLATIONS[browserLang] ? browserLang : 'es');
    this.apply();
    this.updateButtons();
  },

  /**
   * Campo de un artículo en el idioma activo. Los artículos llevan sus
   * traducciones en `a.i18n.<lang>.<campo>` (las genera backend/translator.py);
   * si falta la traducción se sirve el original en español.
   */
  field(article, name) {
    if (!article) return '';
    const tr = article.i18n?.[this.currentLang];
    return (tr && tr[name]) || article[name] || '';
  },

  t(key) {
    return TRANSLATIONS[this.currentLang]?.[key]
      || TRANSLATIONS['es']?.[key]
      || key;
  },

  apply() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      el.textContent = this.t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.t(el.getAttribute('data-i18n-placeholder'));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = this.t(el.getAttribute('data-i18n-title'));
    });
    document.documentElement.lang = this.currentLang;
  },

  setLang(lang) {
    if (!TRANSLATIONS[lang]) return;
    this.currentLang = lang;
    localStorage.setItem('corruptos-lang', lang);
    this.apply();
    this.updateButtons();
    document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
  },

  updateButtons() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === this.currentLang);
    });
  }
};

// Exponer globalmente
window.i18n = i18n;
